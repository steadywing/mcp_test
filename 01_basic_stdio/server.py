from mcp.server.fastmcp import FastMCP

#-------------------------------------------------------------------
# 1. MCP 서버 인스턴스 생성
#-------------------------------------------------------------------
mcp = FastMCP("calculator")

#-------------------------------------------------------------------
# 2. 도구 등록
#-------------------------------------------------------------------
@mcp.tool()
def add(a: int, b: int) -> int:
    """덧셈하는 함수"""
    return a + b

@mcp.tool()
def multiply(a: int, b:int) -> int:
    """곱셈하는 함수"""
    return a * b

#-------------------------------------------------------------------
# 3. MCP 서버 실행
#-------------------------------------------------------------------
if __name__ == "__main__":
    mcp.run(transport="stdio")